window.onscroll = function() {scrollFunction()};

function scrollFunction() {
  const topBar = document.getElementById("topBar");
  // Calculate the scroll distance as a percentage of the total scrollable height
  const scrollDistance = window.scrollY;
  const windowHeight = window.innerHeight;
  const documentHeight = document.body.clientHeight;
  const scrollableHeight = documentHeight - windowHeight;
  const scrollPercentage = (scrollDistance / scrollableHeight) * 100;
  // Set the width of the top bar based on the scroll percentage
  topBar.style.width = scrollPercentage + "%";
}

